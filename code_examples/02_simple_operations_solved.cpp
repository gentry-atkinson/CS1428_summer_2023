#include<iostream>

using namespace std;

int main(){
    //store the result of the operation 2 + 5.3 in a variable named a
    float a = 2+5.3;

    //store the result of the operation 10 % 3 in a variable named b
    int b = 10%3;

    //store the result of the operation 21 / 2 in a variable named c
    int c = 21 / 2;

    //store the result of the operation 21 / 2.0 in a variable named d
    float d = 21 / 2.0;

    //store the result of the operation 4 + 'c' in a variable named e
    char e = 4 + 'c'; //The result of this operation could be a char or an int

    //store the result of the operation 373 - 13.3 in a variable named f
    float f = 373 -13.3;

    cout << "a: " << a << endl;
    cout << "b: " << b << endl;
    cout << "c: " << c << endl;
    cout << "d: " << d << endl;
    cout << "e: " << e << endl;
    cout << "f: " << f << endl;

    return 0;
}
